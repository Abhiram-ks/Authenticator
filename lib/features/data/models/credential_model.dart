import 'package:authenticator/features/domain/entity/credential_entity.dart';
class CredentialModel extends CredentialEntity {
  final String? docId;

  const CredentialModel({
    this.docId,
    required super.uid,
    required super.name,
    super.notes = "",
    super.username,
    super.password,
    super.url,
    super.cardHolderName,
    super.cardNumber,
    super.cardType,
    super.expiryDate,
    super.pin,
    super.postalCode,
    super.firstName,
    super.lastName,
    super.sex,
    super.birthday,
    super.occupation,
    super.company,
    super.department,
    super.jobTitle,
    super.identityAddress,
    super.email,
    super.homePhone,
    super.cellPhone,
    super.addressLine1,
    super.addressLine2,
    super.city,
    super.country,
    super.state,
    super.isAddress = false,
    super.isCreditCard = false,
    super.isIdentity = false,
    super.isLogin = false,
    super.isNotes = false,
  });

  factory CredentialModel.fromJson(Map<String, dynamic> json, String docId) {
    return CredentialModel(
      uid: json['uid'] ?? "",
      name: json['name'] ?? "",
      docId: docId,
      notes: json['notes'] ?? "",
      username: json['username'],
      password: json['password'],
      url: json['url'],
      cardHolderName: json['cardHolderName'],
      cardNumber: json['cardNumber'],
      cardType: json['cardType'],
      expiryDate: json['expiryDate'],
      pin: json['pin'],
      postalCode: json['postalCode'],
      firstName: json['firstName'],
      lastName: json['lastName'],
      sex: json['sex'],
      birthday: json['birthday'],
      occupation: json['occupation'],
      company: json['company'],
      department: json['department'],
      jobTitle: json['jobTitle'],
      identityAddress: json['identityAddress'],
      email: json['email'],
      homePhone: json['homePhone'],
      cellPhone: json['cellPhone'],
      addressLine1: json['addressLine1'],
      addressLine2: json['addressLine2'],
      city: json['city'],
      country: json['country'],
      state: json['state'],
      isAddress: json['isAddress'] ?? false,
      isCreditCard: json['isCreditCard'] ?? false,
      isIdentity: json['isIdentity'] ?? false,
      isLogin: json['isLogin'] ?? false,
      isNotes: json['isNotes'] ?? false,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'uid': uid,
      'docId': docId,
      'name': name,
      'notes': notes,
      'username': username,
      'password': password,
      'url': url,
      'cardHolderName': cardHolderName,
      'cardNumber': cardNumber,
      'cardType': cardType,
      'expiryDate': expiryDate,
      'pin': pin,
      'postalCode': postalCode,
      'firstName': firstName,
      'lastName': lastName,
      'sex': sex,
      'birthday': birthday,
      'occupation': occupation,
      'company': company,
      'department': department,
      'jobTitle': jobTitle,
      'identityAddress': identityAddress,
      'email': email,
      'homePhone': homePhone,
      'cellPhone': cellPhone,
      'addressLine1': addressLine1,
      'addressLine2': addressLine2,
      'city': city,
      'country': country,
      'state': state,
      'isAddress': isAddress,
      'isCreditCard': isCreditCard,
      'isIdentity': isIdentity,
      'isLogin': isLogin,
      'isNotes': isNotes,
    };
  }
}
